/*Looping System
we use loop for execute block of statement again and again

syntax:
for (initialization ; test condition ; int /dec)
{
	code body;

}*/

// print hellworld 20 times

class TTime{									//Declare Class name
	public static void main(String args[]){

	int i;
	for (i=20;i>=1;i--){ 						//Initialize for loop(i++=>i+1)
		System.out.println("Hello World!");		//printable statement
		}
	}
}