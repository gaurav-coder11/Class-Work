//WAP to get sum of first 10 natural numbers
public class SumOfNatNum{  
	public static void main(String[] args){  
		int i, sum = 0;  //executes until the condition returns true  
		for(i = 1; i <= 10; i++){  
			sum = sum + i;  		//adding the value of i into sum variable  
			}  
			System.out.println("Sum of First 10 Natural Numbers is = " + sum);  //prints the sum 
	}  
}  